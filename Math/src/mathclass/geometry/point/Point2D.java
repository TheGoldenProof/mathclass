package mathclass.geometry.point;

public abstract class Point2D {
	
	double x;
	double y;
	
	double r;
	double theta;

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public double getR() {
		return r;
	}

	public double getTheta() {
		return theta;
	}
	
}
